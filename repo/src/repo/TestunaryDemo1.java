package repo;

public class TestunaryDemo1 {

	public static void main(String[] args) {
	int a=1;
		int b=3;
		System.out.println(++a);
		System.out.println(++b);
		System.out.println(a++);
		System.out.println(b++);
		System.out.println(--a);
		System.out.println(--b);
		System.out.println(a--);
		System.out.println(b--);
		System.out.println("***********************8");
		System.out.println(a++ +  b++);
	}

}
